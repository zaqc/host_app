#include "DScope.h"

#include <stdlib.h>
//----------------------------------------------------------------------------

DScope *g_DScope = NULL;
//----------------------------------------------------------------------------

void DScopeInit(void) {
	if (NULL == g_DScope)
		g_DScope = new DScope();
}
//----------------------------------------------------------------------------

void DScopeDone(void) {
	if (NULL != g_DScope) {
		delete g_DScope;
		g_DScope = NULL;
	}
}
//----------------------------------------------------------------------------

//============================================================================
//	DScope
//============================================================================
DScope::DScope() {
}
//----------------------------------------------------------------------------

DScope::~DScope() {
}
//----------------------------------------------------------------------------

